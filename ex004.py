algo = input('Digite algo: ')
print('O valor digitado é alfanumérico? ', algo.isalnum())
print('O valor digitado é numérico? ', algo.isnumeric())
print('O valor digitado é alfabético?', algo.isalpha())
print('O valor digitado está no diminutivo? ', algo.islower())
print('O valor digitado está no aumentativo? ', algo.isupper())


